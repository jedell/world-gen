#include <raylib.h>
#include <math.h>
#include "game.hpp"

int main(void)
{
    // Initialization
    //--------------------------------------------------------------------------------------
    Game *game = new Game();

    game->init();

    //--------------------------------------------------------------------------------------

    // Main game loop
    while (!WindowShouldClose()) // Detect window close button or ESC key
    {
        // Handle Events
        //----------------------------------------------------------------------------------
        game->handleEvents();
        //----------------------------------------------------------------------------------

        // Update
        //----------------------------------------------------------------------------------
        game->update();
        //----------------------------------------------------------------------------------

        // Render
        //----------------------------------------------------------------------------------
        game->render();
        //----------------------------------------------------------------------------------
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------
    CloseWindow(); // Close window and OpenGL context
    //--------------------------------------------------------------------------------------

    return 0;
}